Future<bool> checkLogin() async {
  Completer<bool> complete = Completer();
  FirebaseAuth.instance.authStateChanges().listen((User? user) {
    if (user == null) {
      complete.complete(false);
    } else {
      complete.complete(true);
    }
  });
  return complete.future;
}
